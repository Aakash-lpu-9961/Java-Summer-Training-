package com.company;

public class doWhile_loop_java {
    public static void main(String[] args) {
        System.out.println("do-while loop in Java");
        int a =1;
        int b =1;
        do {
            System.out.println(a);
            a++;
        }while (a<10);

        do {
            System.out.print(" "+b+" ");
            b++;
        }while (b<=50);

    }
}
