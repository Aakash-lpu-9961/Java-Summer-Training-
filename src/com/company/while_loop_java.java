package com.company;

public class while_loop_java {
    public static void main(String[] args) {
        System.out.println("While loops in Java");
        int count=1;
        while (count<=20)
        {
            System.out.println("This is while loop "+"\t");
            count++;
        }
        while (true)
        {
            System.out.println("I am an Infinite While Loop");
        }
    }
}
